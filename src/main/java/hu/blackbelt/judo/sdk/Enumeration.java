package hu.blackbelt.judo.sdk;

public interface Enumeration {

    /**
     * Get the name of this enum constant.
     *
     * @return String
     */
    String getName();

    /**
     * Get the ordinal of this enum constant.
     *
     * @return String
     */
    int getOrdinal();

    /**
     * Get fully qualified name of this enum constant.
     *
     * @return String
     */
    String getFqName();
}
